package com.hhc.barchart.beans;

public class BusinessInfo {
    private String building_name;
    private int office_num;
    private int coffee_num;

    public String getBuilding_name() {
        return building_name;
    }

    public void setBuilding_name(String building_name) {
        this.building_name = building_name;
    }

    public int getOffice_num() {
        return office_num;
    }

    public void setOffice_num(int office_num) {
        this.office_num = office_num;
    }

    public int getCoffee_num() {
        return coffee_num;
    }

    public void setCoffee_num(int coffee_num) {
        this.coffee_num = coffee_num;
    }
}
