package com.hhc.barchart.beans;

public class LifeInfo {
    private String building_name;
    private int restaurant_num;
    private int bank_num;
    private int shopping_num;
    private int hotel_num;

    public String getBuilding_name() {
        return building_name;
    }

    public void setBuilding_name(String building_name) {
        this.building_name = building_name;
    }

    public int getRestaurant_num() {
        return restaurant_num;
    }

    public void setRestaurant_num(int restaurant_num) {
        this.restaurant_num = restaurant_num;
    }

    public int getBank_num() {
        return bank_num;
    }

    public void setBank_num(int bank_num) {
        this.bank_num = bank_num;
    }

    public int getShopping_num() {
        return shopping_num;
    }

    public void setShopping_num(int shopping_num) {
        this.shopping_num = shopping_num;
    }

    public int getHotel_num() {
        return hotel_num;
    }

    public void setHotel_num(int hotel_num) {
        this.hotel_num = hotel_num;
    }
}
